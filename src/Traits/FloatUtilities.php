<?php

namespace Redbastie\Swift\Traits;

trait FloatUtilities
{
    public function floatLeft()
    {
        return $this->class('float-left');
    }

    public function floatSmLeft()
    {
        return $this->class('float-sm-left');
    }

    public function floatMdLeft()
    {
        return $this->class('float-md-left');
    }

    public function floatLgtLeft()
    {
        return $this->class('float-lg-left');
    }

    public function floatXlLeft()
    {
        return $this->class('float-xl-left');
    }

    public function floatRight()
    {
        return $this->class('float-right');
    }

    public function floatSmRight()
    {
        return $this->class('float-sm-right');
    }

    public function floatMdRight()
    {
        return $this->class('float-md-right');
    }

    public function floatLgtRight()
    {
        return $this->class('float-lg-right');
    }

    public function floatXlRight()
    {
        return $this->class('float-xl-right');
    }

    public function floatNone()
    {
        return $this->class('float-none');
    }

    public function floatSmNone()
    {
        return $this->class('float-sm-none');
    }

    public function floatMdNone()
    {
        return $this->class('float-md-none');
    }

    public function floatLgtNone()
    {
        return $this->class('float-lg-none');
    }

    public function floatXlNone()
    {
        return $this->class('float-xl-none');
    }
}
